            <h2>Assignments</h2>

            <p>Generate three distinct directions based on the feedback given in class. Develop a notification system that ties in both formally and conceptually for each of the three proposals. Each direction should take a slightly different approach to the content you're developing for your space and continue to think about structural and navigational aspects of the design to communicate these ideas.</p>

